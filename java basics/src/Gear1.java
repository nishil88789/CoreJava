
public class Gear1 {

	public static void main(String[] args) {
 
		int gear=1;
		
		switch(gear) {
		case 1:
			System.out.println("Minimum Speed");
			break;
		case 2:
			System.out.println("Average Speed");
			break;
		case 3:
		System.out.println("Maximum Speed");
		break;
		case 4:
			System.out.println("Maximum High Speed");
			break;
		case 5:
			System.out.println("High Speed");
		
		}
}
}
