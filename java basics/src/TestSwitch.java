
public class TestSwitch {
public static void main(String[] args) {
	int money = 300;
	switch(money) {
	case 100:
		System.out.println("yes you got 100 rs");
	break;
	case 200:
		System.out.println("yes you got 200 rs");
		break;
	case 300:
		System.out.println("yes you got 300 rs");
		break;
		default:
			System.out.println("sorryyy bad luck");
			break;
		
	
	}
}
}
